#include "main.h"
/**
* Description: print_alphabet - prints the alphabet, in lowercase
*
* main - Entry point
*
* Return: Always 0 (Success)
*/
void print_alphabet(void)
{
char alpha;
for (alpha = 97; alpha <= 122; alpha++)
{
_putchar(alpha);
_putchar('\n');
}
}
int main(void)
{
void print_alphabet(void);
return (0);
}
